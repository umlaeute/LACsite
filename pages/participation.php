<h1>Call for papers</h1>
<p>
Papers can be submitted for review via the
<a href="http://lac.linuxaudio.org/2012/openconf/" rel="external">openconf</a> system.
Templates for paper submission are available for LaTeX and OpenOffice/LibreOffice from <a href="download/lac2012-templates.tar.gz">here</a>.
</p>
<p>
Papers must be written and presented in English, 4&mdash;8 pages, with abstract (50&mdash;100 words) and up to 5 keywords. 
In general a presentation should take 20&mdash;30 minutes followed by 5 minutes discussion.
</p>
<p>
The copyright of the paper remains with the author, but we reserve the right to create printed proceedings from all submitted (and accepted) papers.
</p>
<p>
Below is a list of possible subjects for papers and presentations. This list is
meant as a starting point, not to restrict you.
</p>

<ul class="multicolumn">
  <li>Ambisonics</li>
  <li>Education</li>
  <li>Live performance</li>
  <li>Audio Hardware Support</li>
  <li>Signal Processing</li>
  <li>Music Composition</li>
  <li>Audio Languages</li>
  <li>Sound Synthesis</li>
  <li>Audio Plugins</li>
  <li>MIDI</li>
  <li>Music Production</li>
  <li>Linux Kernel</li>
  <li>Physical Computing</li>
  <li>Interface Design</li>
  <li>Linux Distributions</li>
  <li>Networked Audio</li>
  <li>Video</li>
  <li>Games</li>
  <li>Media Art</li>
  <li>Licensing</li>
</ul>
<br style="clear:both;"/>
<br/>
<p>
We very much welcome practical papers resp. software demos ("how I use Linux Audio applications to create my music/media art").
</p>
<p>
Not all papers need to directly address Linux Audio. There is no limitation for subjects from related fields like open source music applications, open source software for music or sound-research and open-content.
</p>

<p>NB. Papers should be as complete as possible on submission. Until submission closes it is possible to replace/update files in the <a href="http://lac.linuxaudio.org/2012/openconf/" rel="external">openconf</a> system. Submissions will be peer-reviewed and after the notification of acceptance there is a small time-window for updates and last-minute changes.
</p>

