<h1>Traveling to Graz</h1>

<h3>From the airports...</h3>

<p>
The airport in Graz is only about 10 km south of the city. The center is conviniently reached by taxi or public busses.
From the Graz Airport web pages <a href="http://www.flughafen-graz.at/home/index.en.php?change_language=English" rel="external">www.flughafen-graz.at</a> you can find more information about flight schedules and timetables for public transportation.
</p>

<h3>By Train</h3>

<p>
From the central railway station of Graz take tramline 6 towards St.Peter, exit at stop Schulzentrum St. Peter and walk down the Inffeldgasse 200m until you reach the "Studienzentrum". The institute is in the 3rd floor.
</p>

<h3>By Car</h3>

<p>
From North or South: follow A9 Phyrn-Autobahn until the interchange Graz-West, then on A2 Südautobahn towards Vienna.
From East or West: Follow A2 Südautobahn until the interchange Graz-Ost, follow the feeder road until exit Graz-Liebenau, turn right and follow Müngrabenstraße about 2 km, turn right into Sandgasse and follow it till the end, turn right into Petersgasse and after app. 200m turn right into Inffeldgasse.

