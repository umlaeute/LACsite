<h1>Goals</h1>
<ul>
  <li> To bring developers, musicians, composers and other users together </li>
  <li> To discuss the Linux Audio core system </li>
  <li> To discuss Linux Audio applications </li>
  <li> To discuss Open Source music and sound applications </li>
  <li> To inform about Open Source Software for Music</li>
  <li> To inform about Open Content licences</li>
  <li> To collaborate and exchange information </li>
  <li> To improve the software </li>
  <li> To understand the needs of the users </li>
</ul>

<h1>Target audience</h1>
The Linux Audio Conference audience consists of software designers,
programmers, musicians, composers, engineers and others with a
serious interest in Open Source Software for Music and Sound.

<h1>What to expect</h1>
<ul>
 <li>Presentations (preferably about a paper) </li>
 <li>Poster sessions</li>
 <li>Workshops</li>
 <li>Concerts</li>
 <li>Live coding sessions</li>
</ul>

